package com.example.easyflight;

public class FlightData {
    private String departureStation;
    private String arrivalStation;
    private String departureTime;
    private String arrivalTime;
    private String flightHaltTime;
    private String flightCode;
    private String flightPrice;
    private String flightStopNumber;

    public String getDepartureStation() {
        return departureStation;
    }

    public void setDepartureStation(String departureStation) {
        this.departureStation = departureStation;
    }

    public String getArrivalStation() {
        return arrivalStation;
    }

    public void setArrivalStation(String arrivalStation) {
        this.arrivalStation = arrivalStation;
    }

    public String getDepartureTime() {
        return departureTime;
    }

    public void setDepartureTime(String departureTime) {
        this.departureTime = departureTime;
    }

    public String getArrivalTime() {
        return arrivalTime;
    }

    public void setArrivalTime(String arrivalTime) {
        this.arrivalTime = arrivalTime;
    }

    public String getFlightHaltTime() {
        return flightHaltTime;
    }

    public void setFlightHaltTime(String flightHaltTime) {
        this.flightHaltTime = flightHaltTime;
    }

    public String getFlightCode() {
        return flightCode;
    }

    public void setFlightCode(String flightCode) {
        this.flightCode = flightCode;
    }

    public String getFlightPrice() {
        return flightPrice;
    }

    public void setFlightPrice(String flightPrice) {
        this.flightPrice = flightPrice;
    }

    public String getFlightStopNumber() {
        return flightStopNumber;
    }

    public void setFlightStopNumber(String flightStopNumber) {
        this.flightStopNumber = flightStopNumber;
    }
}
